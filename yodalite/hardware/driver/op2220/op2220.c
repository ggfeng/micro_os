void Op02220_Initialize_from_Saved_Settings(uint8_t ch)
{
	uint8_t tmp[COUNT_SIZE];
	uint8_t reg_data, reg_dram_rfsh;
	uint16_t i;

	// over-ride keep dc balance
	SCCB_Write_Char(ch,OP2220_DEVICE_ADR, VSET, 0x4A);
	
	// need to over-ride and enable PLL
	SCCB_Write_Char(ch,OP2220_DEVICE_ADR, Phy_Ctrl0, 0x32);

	// 1. first segment registers
	for (i = 0; i < REGISTER_COUNT/COUNT_SIZE; i++)
	{
#if	AT24ChipQuantity == 1
		SCCB_Burst_Read_Char(ch, AT24_DEVICE_ADR, NV_REGISTERS + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#else
		memcpy(&tmp[0], &OP02220_DefReg[NV_REGISTERS + i * COUNT_SIZE], COUNT_SIZE);
		//µ÷ÕûÆÁ·½Ïò
		if(i == 0)
		{
				tmp[6] = 0x40;
		}
#endif
		// Only for the set containing register LED_Ctrl
		if (i == (LED_Ctrl/COUNT_SIZE))
			// keep all disabled
			tmp[LED_Ctrl % COUNT_SIZE] = 0x70;
		// Only for the set containing register Table_ctrl
		if (i == (Table_ctrl/COUNT_SIZE))
			// enable direct access
			tmp[Table_ctrl % COUNT_SIZE] = 0x29;
		SCCB_Burst_Write_Char(ch, OP2220_DEVICE_ADR, REG_ADDR_START + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
	}

	// 2. next segment MiPi
	for (i = 0; i < MIPI_REG_COUNT/COUNT_SIZE; i++)
	{
#if	AT24ChipQuantity == 1
		SCCB_Burst_Read_Char(ch, AT24_DEVICE_ADR, NV_MIPI_REG + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#else
		memcpy(&tmp[0], &OP02220_DefReg[NV_MIPI_REG + i * COUNT_SIZE], COUNT_SIZE);
#endif
		SCCB_Burst_Write_Char(ch, OP2220_DEVICE_ADR, MIPI_REGISTER_START + i * COUNT_SIZE, &tmp[0], COUNT_SIZE);
	}
	
	// 3. next segment Gamma
	for (i = 0; i < GAMMA_COUNT/COUNT_SIZE; i++)
	{
#if	AT24ChipQuantity == 1
		SCCB_Burst_Read_Char(ch, AT24_DEVICE_ADR, NV_GAMMA_RED + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#else
		memcpy(&tmp[0], &OP02220_DefReg[NV_GAMMA_RED + i * COUNT_SIZE], COUNT_SIZE);
#endif
		SCCB_Burst_Write_Char(ch, OP2220_DEVICE_ADR, GAMMA_TABLE_START + RED_SELECT + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#if	AT24ChipQuantity == 1
		SCCB_Burst_Read_Char(ch, AT24_DEVICE_ADR, NV_GAMMA_GREEN + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#else
		memcpy(&tmp[0], &OP02220_DefReg[NV_GAMMA_GREEN + i * COUNT_SIZE], COUNT_SIZE);
#endif
		SCCB_Burst_Write_Char(ch, OP2220_DEVICE_ADR, GAMMA_TABLE_START + GREEN_SELECT + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#if	AT24ChipQuantity == 1
		SCCB_Burst_Read_Char(ch, AT24_DEVICE_ADR, NV_GAMMA_BLUE + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#else
		memcpy(&tmp[0], &OP02220_DefReg[NV_GAMMA_BLUE + i * COUNT_SIZE], COUNT_SIZE);
#endif
		SCCB_Burst_Write_Char(ch, OP2220_DEVICE_ADR, GAMMA_TABLE_START + BLUE_SELECT + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
	}

	// 4. next segment PWM
	for (i = 0; i < PWM_COUNT/COUNT_SIZE; i++)
	{
#if	AT24ChipQuantity == 1
		SCCB_Burst_Read_Char(ch, AT24_DEVICE_ADR, NV_PWM_RED + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#else
		memcpy(&tmp[0], &OP02220_DefReg[NV_PWM_RED + i * COUNT_SIZE], COUNT_SIZE);
#endif
		SCCB_Burst_Write_Char(ch, OP2220_DEVICE_ADR, PWM_TABLE_START + RED_SELECT + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#if	AT24ChipQuantity == 1
		SCCB_Burst_Read_Char(ch, AT24_DEVICE_ADR, NV_PWM_GREEN + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#else
		memcpy(&tmp[0], &OP02220_DefReg[NV_PWM_GREEN + i * COUNT_SIZE], COUNT_SIZE);
#endif
		SCCB_Burst_Write_Char(ch, OP2220_DEVICE_ADR, PWM_TABLE_START + GREEN_SELECT + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#if	AT24ChipQuantity == 1
		SCCB_Burst_Read_Char(ch, AT24_DEVICE_ADR, NV_PWM_BLUE + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#else
		memcpy(&tmp[0], &OP02220_DefReg[NV_PWM_BLUE + i * COUNT_SIZE], COUNT_SIZE);
#endif
		SCCB_Burst_Write_Char(ch, OP2220_DEVICE_ADR, PWM_TABLE_START + BLUE_SELECT + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
	}

	// 5. next segment LSB
	for (i = 0; i < LSB_TABLE_COUNT/COUNT_SIZE; i++)
	{
#if	AT24ChipQuantity == 1
		SCCB_Burst_Read_Char(ch, AT24_DEVICE_ADR, NV_LSB_TABLE + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#else
		memcpy(&tmp[0], &OP02220_DefReg[NV_LSB_TABLE + i * COUNT_SIZE], COUNT_SIZE);
#endif
		SCCB_Burst_Write_Char(ch, OP2220_DEVICE_ADR, LSB_TABLE_START + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
	}

	// 6. next segment sequence
	// save setting
	SCCB_Read_Char(ch, OP2220_DEVICE_ADR, DRAM_Rfsh, &reg_dram_rfsh);
	for (i = 0; i < SEQUENCE_COUNT/COUNT_SIZE; i++)
	{
#if	AT24ChipQuantity == 1
		SCCB_Burst_Read_Char(ch, AT24_DEVICE_ADR, NV_SEQUENCE + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
#else
		memcpy(&tmp[0], &OP02220_DefReg[NV_SEQUENCE + i * COUNT_SIZE], COUNT_SIZE);
#endif
		// disable refresh
		SCCB_Write_Char(ch, OP2220_DEVICE_ADR, DRAM_Rfsh, 0x00);
		// point to sequence table
		SCCB_Write_Char(ch, OP2220_DEVICE_ADR, SPECIAL_REG_SELECT, 0x02);

		SCCB_Burst_Write_Char(ch, OP2220_DEVICE_ADR, MEM_ADDR_START + i * COUNT_SIZE, &tmp[0], (uint16_t)COUNT_SIZE);
		// put back to default
		SCCB_Write_Char(ch, OP2220_DEVICE_ADR, SPECIAL_REG_SELECT, 0x00);
		// put back refresh value
		SCCB_Write_Char(ch, OP2220_DEVICE_ADR, DRAM_Rfsh, reg_dram_rfsh);
		// wait
//		DelaymS(10);
		delay_ms_o(100);
	}

	// 7. next write missing parts
	// write missed registers from NVRAM
#if	AT24ChipQuantity == 1
	SCCB_Read_Char(ch, AT24_DEVICE_ADR, NV_REGISTERS + Table_ctrl, &reg_data);
#else
	memcpy(&reg_data, &OP02220_DefReg[NV_REGISTERS + Table_ctrl], 1);
#endif
	// put back value
	SCCB_Write_Char(ch, OP2220_DEVICE_ADR, Table_ctrl, reg_data);
	// Clear border
	SCCB_Write_Char(ch, OP2220_DEVICE_ADR, MEM_MODE_REG, 0x88);
//	DelaymS(16);
		delay_ms_o(500);
	// put value back
	SCCB_Write_Char(ch, OP2220_DEVICE_ADR, MEM_MODE_REG, 0x00);
#if	AT24ChipQuantity == 1
	SCCB_Read_Char(ch, AT24_DEVICE_ADR, NV_REGISTERS + LED_Ctrl, &reg_data);
#else
 	memcpy(&reg_data, &OP02220_DefReg[NV_REGISTERS + LED_Ctrl], 1);
#endif
	// put back value, enable LEDs
	SCCB_Write_Char(ch, OP2220_DEVICE_ADR, LED_Ctrl,reg_data);	
}

