unsigned char DevOp02220_Init(uint8_t ch)
{
	BOOLEAN_TypeDef rtn = 0;
	uint8_t buf[3] = {0,0,0};

	P_OP02220_Reset0_H;
	P_OP02220_V1S0_Set_H;
	delay_ms_o(500);
	P_OP02220_Reset0_L;
	delay_ms_o(500);

	//Read DeviceID
	rtn = SCCB_Burst_Read_Char(ch,OP2220_DEVICE_ADR, 0x00, &buf[0], 3);
	if(rtn)
	{
		SCCB_Burst_Read_Char(ch,OP2220_DEVICE_ADR,0x008c,&buf[0],1);
		Op02220_Initialize_from_Saved_Settings(ch);
	}
	else
	{
		return 0;
	}

	return rtn;
}
